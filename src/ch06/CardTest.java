package ch06;

	class Card	{
		String kind;
		int number;
		static int width = 100;
		static int heigt = 250;
	}
	
	class CardTest	{
		public static void main (String args[]) {
			System.out.println("Card.width = "+ Card.width);
			System.out.println("Card.heigt ="+ Card.heigt);
			
			Card c1 = new Card();
			c1.kind = "Heart";
			c1.number = 7;
			
			Card c2 = new Card();
			c2.kind = "Spade";
			c2.number = 4;
			
			System.out.println("c1은 " + c1.kind + ", " + c1.number + "이며, 크기는 ("+ c1.width + ", " + c1.heigt + ")");
			System.out.println("c2는 " + c2.kind + ", " + c2.number + "이며, 크기는 ("+ c2.width + ", " + c2.heigt + ")");
			System.out.println("c1의 width와 heigt를 각각 50, 80으로 변경합니다.");
			c1.width = 50;
			c2.heigt= 80;
			
			System.out.println("c1은 " + c1.kind + ", " + c1.number + "이며, 크기는 ("+ c1.width + ", " + c1.heigt + ")");
			System.out.println("c2는 " + c2.kind + ", " + c2.number + "이며, 크기는 ("+ c2.width + ", " + c2.heigt + ")");
			
		}
	}