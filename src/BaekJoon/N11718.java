package BaekJoon;

import java.util.Scanner;

//그대로 출력하기
public class N11718 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        while(sc.hasNextLine()){
            String str = sc.nextLine();
            System.out.println(str);
        }

        sc.close();

    }
}
